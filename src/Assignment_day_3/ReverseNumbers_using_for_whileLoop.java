package Assignment_day_3;

public class ReverseNumbers_using_for_whileLoop {

    public static void main(String[] args) {

        for (int i = 10; i >= 1; i--){
            System.out.println(i + " ");
        }
        System.out.println();

        // Using WHile Loop

        int j = 10;

        while (j >= 1){
            System.out.println(j + " ");
            j--;
        }
        System.out.println();
    }
}
